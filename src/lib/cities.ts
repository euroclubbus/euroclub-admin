export const CITIES: CityRef[] = RAW
  .map(([id, name]) => ({ id: String(id), name }))
  .sort((a, b) => a.name.localeCompare(b.name, "uk"));

export function cityName(id: string): string {
  return CITIES.find((c) => c.id === id)?.name ?? id;
}
